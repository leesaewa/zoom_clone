const welcome = document.getElementById("wrapper");
const welcomeForm = welcome.querySelector("form");
const roomListHide = document.getElementById("room-list");

async function initCall() {
  welcome.hidden = true;
  roomListHide.hidden = true;
  call.hidden = false;
  await getMedia();
  makeConnection();
}

async function handleWelcomeSubmit(event) {
  event.preventDefault();
  const input = welcomeForm.querySelector("input");
  await initCall();
  socket.emit("join_room", input.value);
  roomName = input.value;
  input.value = "";
  const h4 = document.querySelector("#call h4");
  h4.innerText = `This Dueling Club Name Is: ${roomName}`;
}

welcomeForm.addEventListener("submit", handleWelcomeSubmit);

